// For Loops --->		for(int i=0; i < 10; i++) {do this stuff}
/*				   |   x   |   y   |  z |

x = declare var that we will use as a loop counter, runs before loop runs

y = loop will run if this condition is true (like inside the () in while(*THIS*) )

z = do this after loop is run once

*/


#include <iostream>
using namespace std;

int main()
 {

 	for(int i=1; i <= 10; i++ )
 	 {
 	 	cout << "This is loop run number " << i << endl;
 	 }

	return 0;

 }
